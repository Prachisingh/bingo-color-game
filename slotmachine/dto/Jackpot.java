package slotmachine.dto;

public class Jackpot {
    private int jackpotValue;
    private int jackpotName;

    public int getJackpotValue() {
        return jackpotValue;
    }

    public void setJackpotValue(int jackpotValue) {
        this.jackpotValue = jackpotValue;
    }

    public int getJackpotName() {
        return jackpotName;
    }

    public void setJackpotName(int jackpotName) {
        this.jackpotName = jackpotName;
    }
}
